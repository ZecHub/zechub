// Regenerate translations/TRANSLATION_STATUS.md from the filesystem.
// Usage: node scripts/gen-translation-status.mjs   (run from the content repo root)
//
// Lists every English wiki page under site/ (excluding site/zechubglobal/, which
// is existing community content in other languages) and marks whether an Italian
// translation exists under translations/it/site/. Keeps the coverage tracker in
// sync with reality — re-run after adding translations.
import fs from "node:fs";
import path from "node:path";

const LOCALE = "it";
const LIVE = "https://zechub-wiki-i18n.vercel.app";

function walk(dir) {
  let out = [];
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) out = out.concat(walk(p));
    else if (e.name.endsWith(".md")) out.push(p);
  }
  return out;
}

function route(rel) {
  return "/" + LOCALE + "/" + rel.replace(/\.md$/, "").toLowerCase().replace(/_/g, "-");
}

const all = walk("site")
  .map((p) => p.replace(/^site\//, ""))
  .filter((p) => !p.startsWith("zechubglobal/"))
  .sort();

const cats = {};
let done = 0;
let tot = 0;
for (const rel of all) {
  const cat = rel.split("/")[0];
  const isDone = fs.existsSync(`translations/${LOCALE}/site/${rel}`);
  (cats[cat] = cats[cat] || []).push([rel, isDone]);
  tot++;
  if (isDone) done++;
}

let out = `# Italian Translation Coverage (${LOCALE})\n\n`;
out += "Auto-generated by `scripts/gen-translation-status.mjs`. Excludes\n";
out += "`site/zechubglobal/` (existing community content already in other languages).\n\n";
out += `Live pilot: ${LIVE}\n\n`;
out += `**Overall (core wiki): ${done}/${tot} pages (${Math.round((done / tot) * 100)}%)**\n\n`;
out += "| Category | Done | Total |\n|---|---|---|\n";
for (const c of Object.keys(cats).sort()) {
  const l = cats[c];
  out += `| ${c} | ${l.filter((x) => x[1]).length} | ${l.length} |\n`;
}
out += "\n---\n\n## Pages\n\nLegend: translated [x] / missing [ ]\n\n";
for (const c of Object.keys(cats).sort()) {
  const l = cats[c];
  const d = l.filter((x) => x[1]).length;
  out += `### ${c} (${d}/${l.length})\n\n`;
  for (const [rel, isDone] of l) {
    out += `- [${isDone ? "x" : " "}] \`${rel}\`${isDone ? " — " + route(rel) : ""}\n`;
  }
  out += "\n";
}

fs.writeFileSync(`translations/TRANSLATION_STATUS.md`, out);
console.log(`Wrote translations/TRANSLATION_STATUS.md — ${done}/${tot} pages translated`);
